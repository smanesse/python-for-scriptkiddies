l = [3, 1, 4, 1, 5, 9, 2, 6, 5, 3, 5]

# iterate over items in a list
for item in l:
    print(item)

# iterate all numbers up to (not including) 10
for i in range(0, 10):
    print(i)

# while (condition) will do the thing until the condition is False
i = 0
while i < 10:
    print(i)
    i = i+1


